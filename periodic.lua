_nametosy = {}
_numtosy = {} -- number to symbol - lowercase
numtosy = {} -- number to symbol - formatted

for k, v in pairs(_periodic) do
	_nametosy[v["name"]:lower()] = k -- make symbol to name table
	_numtosy[v["number"]] = k -- make number to symbol table - lowercase
	numtosy[v["number"]] = k:sub(1,1):upper() .. k:sub(2) -- make number to symbol table - formatted
end


numtoname = setmetatable({}, {
	__index = function(numtoname, key)
		return _periodic[_numtosy[key]].name
	end
})

nametosy = setmetatable({}, { -- name to symbol
	__index = function(nametosy, key)
		return _nametosy[string.lower(key)]
	end
})

periodic = setmetatable({}, {
	__index = function(periodic, key)
		k = key:lower()
		if _periodic[k] then return _periodic[k]
		else return _periodic[_nametosy[k]] end
	end
})

pdt = periodic
ntn = numtoname
nts = numtosy

